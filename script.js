if (heroMedia && !motionQuery.matches) {
  let pointerFrame = null;
  let nextPointerX = 0;
  let nextPointerY = 0;

  const syncHeroPointer = () => {
    heroMedia.style.setProperty("--pointer-x", `${nextPointerX.toFixed(2)}px`);
    heroMedia.style.setProperty("--pointer-y", `${nextPointerY.toFixed(2)}px`);
    pointerFrame = null;
  };

  const queueHeroPointer = () => {
    if (pointerFrame !== null) return;
    pointerFrame = window.requestAnimationFrame(syncHeroPointer);
  };

  heroMedia.addEventListener("pointermove", (event) => {
    const bounds = heroMedia.getBoundingClientRect();
    nextPointerX = ((event.clientX - bounds.left) / bounds.width - 0.5) * 10;
    nextPointerY = ((event.clientY - bounds.top) / bounds.height - 0.5) * 8;
    queueHeroPointer();
  });

  heroMedia.addEventListener("pointerleave", () => {
    nextPointerX = 0;
    nextPointerY = 0;
    queueHeroPointer();
  });
}

